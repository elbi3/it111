public class Concatenation {
    public static void main(String[] args) {
        String color = "blue";
        System.out.println("My favorite color for dishes is " +color);

        int money = 2_030_300;
        System.out.println("A house might cost $" +money+ " dollars!");

        System.out.println(color+" "+money);

        System.out.println("If I had a hundred dishes, would they all be " +color+ " and would they fill a house that cost $" +money+ " dollars?");
    }

}
